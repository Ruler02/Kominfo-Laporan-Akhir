NEGATION_WORDS = {"tidak", "bukan", "tanpa", "jangan", "kurang", "belum"}

def lexicon_label(tokens: list) -> str:
    """
    Hitung skor sentimen berbasis lexicon dengan penanganan negasi sederhana:
    jika sebuah kata sentimen didahului kata negasi dalam jarak 1 token,
    polaritasnya dibalik. Contoh: 'tidak baik' -> dihitung sebagai negatif,
    bukan positif.
    """
    score = 0
    for i, tok in enumerate(tokens):
        is_negated = i > 0 and tokens[i - 1] in NEGATION_WORDS
        if tok in POSITIVE_WORDS:
            score += -1 if is_negated else 1
        elif tok in NEGATIVE_WORDS:
            score += 1 if is_negated else -1

    if score > 0:
        return "Positif"
    elif score < 0:
        return "Negatif"
    else:
        return "Netral"


