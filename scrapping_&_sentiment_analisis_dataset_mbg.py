def clean_text(text: str) -> str:
    """Bersihkan teks dari whitespace berlebih dan karakter tidak perlu."""
    text = re.sub(r'\s+', ' ', text)
    text = re.sub(r'\n{3,}', '\n\n', text)
    return text.strip()


def extract_content_from_html(html: str) -> str:
    """
    Ekstrak konten/deskripsi utama dari HTML artikel.
    Mencoba berbagai selector CSS secara berurutan.
    """
    soup = BeautifulSoup(html, "lxml")

    # Buang elemen noise
    for tag in soup(["script", "style", "aside", "nav", "footer",
                     "header", "figure", "figcaption", "iframe",
                     "noscript", "form", "button"]):
        tag.decompose()

    # Coba tiap selector
    for selector in CONTENT_SELECTORS:
        try:
            container = soup.select_one(selector)
            if not container:
                continue

            paragraphs = container.find_all("p")
            texts = [p.get_text(separator=" ", strip=True) for p in paragraphs
                     if len(p.get_text(strip=True)) > 40]

            if texts:
                content = " ".join(texts)
                return clean_text(content)
        except Exception:
            continue

    # Fallback: ambil semua <p> di body
    body = soup.find("body")
    if body:
        paragraphs = body.find_all("p")
        texts = [p.get_text(separator=" ", strip=True) for p in paragraphs
                 if len(p.get_text(strip=True)) > 40]
        if texts:
            return clean_text(" ".join(texts[:20]))  # Max 20 paragraf

    return ""


def fetch_article_content(article: dict) -> dict:
    """
    Fetch konten penuh satu artikel.
    Mengembalikan article dict yang sudah dilengkapi field 'deskripsi'.
    """
    url = article.get("link", "")
    if not url or not url.startswith("http"):
        article["status_fetch"] = "url_invalid"
        return article

    try:
        time.sleep(random.uniform(CONTENT_DELAY_MIN, CONTENT_DELAY_MAX))
        resp = requests.get(
            url,
            headers=get_headers(),
            timeout=CONTENT_TIMEOUT,
            allow_redirects=True
        )
        resp.raise_for_status()

        # Pastikan ini HTML
        content_type = resp.headers.get("Content-Type", "")
        if "text/html" not in content_type:
            article["status_fetch"] = "bukan_html"
            return article

        content = extract_content_from_html(resp.text)

        if content:
            article["deskripsi"]    = content
            article["jumlah_kata"]  = len(content.split())
            article["status_fetch"] = "berhasil"
        else:
            # Fallback ke snippet jika konten tidak ditemukan
            article["deskripsi"]    = article.get("snippet", "")
            article["jumlah_kata"]  = len(article["deskripsi"].split())
            article["status_fetch"] = "fallback_snippet"

    except requests.exceptions.Timeout:
        article["deskripsi"]    = article.get("snippet", "")
        article["status_fetch"] = "timeout"
    except requests.exceptions.TooManyRedirects:
        article["deskripsi"]    = article.get("snippet", "")
        article["status_fetch"] = "redirect_loop"
    except requests.exceptions.RequestException as e:
        article["deskripsi"]    = article.get("snippet", "")
        article["status_fetch"] = f"error"
    except Exception as e:
        article["deskripsi"]    = article.get("snippet", "")
        article["status_fetch"] = "parse_error"

    return article


def fetch_all_contents(articles: list[dict]) -> list[dict]:
    """
    Fetch konten semua artikel secara paralel menggunakan ThreadPoolExecutor.
    """
    total = len(articles)
    results = [None] * total
    completed = 0

    log.info(f"\nMulai fetch konten {total} artikel "
             f"({MAX_CONTENT_WORKERS} thread paralel)...")

    with ThreadPoolExecutor(max_workers=MAX_CONTENT_WORKERS) as executor:
        future_to_idx = {
            executor.submit(fetch_article_content, art): i
            for i, art in enumerate(articles)
        }

        for future in as_completed(future_to_idx):
            idx = future_to_idx[future]
            try:
                results[idx] = future.result()
            except Exception as e:
                results[idx] = articles[idx]
                results[idx]["status_fetch"] = "exception"

            completed += 1

            # Log progress setiap 25 artikel
            if completed % 25 == 0 or completed == total:
                berhasil = sum(1 for r in results if r and r["status_fetch"] == "berhasil")
                log.info(f"  [{completed}/{total}] Berhasil: {berhasil} | "
                         f"Fallback/Error: {completed - berhasil}")

            # Checkpoint setiap 50 artikel
            if completed % 50 == 0:
                done = [r for r in results if r is not None]
                save_checkpoint(done)

    return [r for r in results if r is not None]


