def print_top_words(token_lists, label="Keseluruhan", top_n=TOP_N_WORDS):
    all_tokens = [tok for tokens in token_lists for tok in tokens]
    counter = Counter(all_tokens)
    print(f"\n--- Top {top_n} kata paling sering muncul ({label}) ---")
    for word, freq in counter.most_common(top_n):
        print(f"{word:<20} {freq}")
    return counter


def save_wordcloud(counter: Counter, filename: str, title: str):
    if not HAS_WORDCLOUD:
        print(f"[!] Library wordcloud tidak terpasang, lewati pembuatan {filename}. "
              f"Install dengan: pip install wordcloud --break-system-packages")
        return
    if len(counter) == 0:
        return
    wc = WordCloud(width=900, height=500, background_color="white",
                    colormap="viridis").generate_from_frequencies(counter)
    plt.figure(figsize=(8, 5))
    plt.imshow(wc, interpolation="bilinear")
    plt.axis("off")
    plt.title(title)
    plt.tight_layout()
    plt.savefig(filename)
    plt.close()
    print(f"Wordcloud disimpan ke {filename}")


