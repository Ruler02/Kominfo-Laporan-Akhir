# ==========================================================================
def main():
    # ---- Load data ----
    df = pd.read_csv(INPUT_CSV)
    print(f"Jumlah baris awal: {len(df)}")

    # ---- Bangun kolom teks final dengan fallback, supaya data tidak terbuang ----
    # deskripsi -> snippet -> judul (urutan prioritas konten paling kaya informasi)
    def pick_text(row):
        for col in ["deskripsi", "snippet", "judul"]:
            val = row.get(col)
            if pd.notna(val) and str(val).strip() != "":
                return str(val)
        return ""

    df["text_raw"] = df.apply(pick_text, axis=1)
    df = df[df["text_raw"].str.strip() != ""].reset_index(drop=True)
    print(f"Jumlah baris setelah fallback teks (sebelumnya banyak terbuang karena "
          f"deskripsi kosong): {len(df)}")

    # ---- 1. Preprocessing ----
    df["clean_text"] = df["text_raw"].apply(clean_text)

    # ---- 2. Tokenizing + Stopword Removal + Stemming ----
    df["tokens"] = df["clean_text"].apply(tokenize_and_remove_stopword)
    df["processed_text"] = df["tokens"].apply(lambda toks: " ".join(toks))

    # ---- Analisis kata terpopuler SEBELUM labeling (gambaran umum isu) ----
    print_top_words(df["tokens"], label="Seluruh Berita")

    # ---- Labeling sentimen (lexicon + negasi) ----
    df["sentiment"] = df["tokens"].apply(lexicon_label)
    print("\nDistribusi label sentimen (lexicon diperluas + negasi):")
    print(df["sentiment"].value_counts())

    # ---- Kata terpopuler PER KELAS sentimen + wordcloud ----
    overall_counters = {}
    for label in ["Positif", "Negatif", "Netral"]:
        subset_tokens = df.loc[df["sentiment"] == label, "tokens"]
        counter = print_top_words(subset_tokens, label=label, top_n=15)
        overall_counters[label] = counter
        save_wordcloud(counter, f"wordcloud_{label.lower()}.png", f"Wordcloud Sentimen {label}")

    # ---- 3. TF-IDF ----
    tfidf = TfidfVectorizer(max_features=3000, min_df=2, ngram_range=(1, 2))
    X_tfidf = tfidf.fit_transform(df["processed_text"])
    print(f"\nShape TF-IDF matrix: {X_tfidf.shape}")

    # Top kata berdasarkan rata-rata skor TF-IDF (kata paling "khas"/penting
    # secara keseluruhan korpus, bisa beda dari yang sering muncul saja)
    mean_tfidf = np.asarray(X_tfidf.mean(axis=0)).ravel()
    terms = tfidf.get_feature_names_out()
    top_idx = mean_tfidf.argsort()[::-1][:TOP_N_WORDS]
    print(f"\n--- Top {TOP_N_WORDS} kata berdasarkan rata-rata skor TF-IDF ---")
    for idx in top_idx:
        print(f"{terms[idx]:<25} {mean_tfidf[idx]:.4f}")

    # ==========================================================
    # 4. CLASSIFICATION (Positif / Negatif / Netral)
    #    Pakai Stratified K-Fold Cross Validation karena data kecil
    #    -> single train-test split (16 data test) tidak representatif
    # ==========================================================
    y = df["sentiment"]
    min_class_size = y.value_counts().min()
    n_folds = max(2, min(N_FOLDS, min_class_size))
    print(f"\nMenggunakan {n_folds}-Fold Stratified Cross Validation "
          f"(disesuaikan dgn kelas minoritas berjumlah {min_class_size})")

    skf = StratifiedKFold(n_splits=n_folds, shuffle=True, random_state=RANDOM_STATE)

    # --- Model 1: ComplementNB (lebih tahan terhadap class imbalance dibanding MultinomialNB) ---
    nb_model = ComplementNB()
    y_pred_nb = cross_val_predict(nb_model, X_tfidf, y, cv=skf)

    print("\n=== Hasil Klasifikasi (Cross-Validation): ComplementNB ===")
    print(f"Akurasi: {accuracy_score(y, y_pred_nb):.4f}")
    print(f"F1-macro: {f1_score(y, y_pred_nb, average='macro'):.4f}")
    print(classification_report(y, y_pred_nb))
    print("Confusion Matrix:\n", confusion_matrix(y, y_pred_nb, labels=["Positif", "Netral", "Negatif"]))

    # --- Model 2: SVM dengan class_weight='balanced' (memberi bobot lebih ke kelas minoritas) ---
    svm_model = SVC(kernel="linear", class_weight="balanced", random_state=RANDOM_STATE)
    y_pred_svm = cross_val_predict(svm_model, X_tfidf, y, cv=skf)

    print("\n=== Hasil Klasifikasi (Cross-Validation): SVM (class_weight=balanced) ===")
    print(f"Akurasi: {accuracy_score(y, y_pred_svm):.4f}")
    print(f"F1-macro: {f1_score(y, y_pred_svm, average='macro'):.4f}")
    print(classification_report(y, y_pred_svm))
    print("Confusion Matrix:\n", confusion_matrix(y, y_pred_svm, labels=["Positif", "Netral", "Negatif"]))

    # ==========================================================
    # 5. CLUSTERING (KMeans) -> pengelompokan topik berita
    # ==========================================================
    inertia, sil_scores = [], []
    k_range = range(2, 8)
    for k in k_range:
        km_temp = KMeans(n_clusters=k, random_state=RANDOM_STATE, n_init=10)
        labels_temp = km_temp.fit_predict(X_tfidf)
        inertia.append(km_temp.inertia_)
        sil_scores.append(silhouette_score(X_tfidf, labels_temp))

    fig, axes = plt.subplots(1, 2, figsize=(12, 4))
    axes[0].plot(list(k_range), inertia, marker="o")
    axes[0].set_xlabel("Jumlah Cluster (k)")
    axes[0].set_ylabel("Inertia")
    axes[0].set_title("Elbow Method")

    axes[1].plot(list(k_range), sil_scores, marker="o", color="orange")
    axes[1].set_xlabel("Jumlah Cluster (k)")
    axes[1].set_ylabel("Silhouette Score")
    axes[1].set_title("Silhouette Score per k")
    plt.tight_layout()
    plt.savefig("elbow_silhouette.png")
    plt.close()
    print("\nGrafik elbow + silhouette disimpan ke elbow_silhouette.png")
    print(f"Silhouette score per k: {dict(zip(k_range, [round(s,4) for s in sil_scores]))}")

    # Clustering final dengan N_CLUSTERS
    kmeans = KMeans(n_clusters=N_CLUSTERS, random_state=RANDOM_STATE, n_init=10)
    df["cluster"] = kmeans.fit_predict(X_tfidf)

    final_sil = silhouette_score(X_tfidf, df["cluster"])
    print(f"\nSilhouette Score final (k={N_CLUSTERS}): {final_sil:.4f}")

    order_centroids = kmeans.cluster_centers_.argsort()[:, ::-1]
    print(f"\n=== Top 10 Kata Kunci per Cluster (k={N_CLUSTERS}) ===")
    for i in range(N_CLUSTERS):
        top_terms = [terms[ind] for ind in order_centroids[i, :10]]
        n_docs = (df["cluster"] == i).sum()
        print(f"Cluster {i} ({n_docs} dokumen): {', '.join(top_terms)}")

    # Silang cluster x sentimen -> insight tambahan untuk pembahasan laporan
    print("\n=== Tabulasi Cluster vs Sentimen ===")
    print(pd.crosstab(df["cluster"], df["sentiment"]))

    # Visualisasi cluster (PCA 2D)
    pca = PCA(n_components=2, random_state=RANDOM_STATE)
    X_pca = pca.fit_transform(X_tfidf.toarray())

    plt.figure(figsize=(7, 5))
    scatter = plt.scatter(X_pca[:, 0], X_pca[:, 1], c=df["cluster"], cmap="viridis", alpha=0.7)
    plt.title("Visualisasi Cluster Berita MBG (PCA 2D)")
    plt.xlabel("PC1")
    plt.ylabel("PC2")
    plt.colorbar(scatter, label="Cluster")
    plt.tight_layout()
    plt.savefig("cluster_visualization.png")
    plt.close()
    print("Visualisasi cluster disimpan ke cluster_visualization.png")

    # ---- Simpan hasil akhir ----
    df.drop(columns=["tokens"]).to_csv("hasil_analisis_sentimen_mbg.csv", index=False)
    print("\nHasil lengkap (sentimen + cluster) disimpan ke hasil_analisis_sentimen_mbg.csv")


if __name__ == "__main__":
    main()