CUSTOM_STOPWORDS = {
    "mbg", "makan", "bergizi", "gratis", "program", "berita", "kompas",
    "detik", "tribun", "kominfo", "pemerintah", "kata", "menurut", "ujar",
    "kepada", "yakni", "sebut", "jelas", "papar", "ungkap", "tutur"
}

def tokenize_and_remove_stopword(text: str) -> list:
    """Tokenisasi + stopword removal + stemming. Return LIST token (bukan string)
    supaya bisa dipakai juga untuk negation handling & word frequency."""
    text_no_stopword = stopword_remover.remove(text)
    tokens = text_no_stopword.split()
    tokens = [t for t in tokens if t not in CUSTOM_STOPWORDS and len(t) > 2]
    stemmed_tokens = [stemmer.stem(t) for t in tokens]
    return stemmed_tokens


