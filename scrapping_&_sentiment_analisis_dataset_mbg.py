def build_serpapi_params(query: str, page: int) -> dict:
    return {
        "engine":  "google_news",
        "q":       query,
        "hl":      LANGUAGE,
        "gl":      COUNTRY,
        "num":     10,
        "start":   (page - 1) * 10,
        "api_key": API_KEY,
    }


def fetch_serpapi_page(page: int) -> list[dict]:
    """Fetch satu halaman dari SerpAPI, kembalikan list artikel mentah."""
    params = build_serpapi_params(QUERY, page)
    try:
        resp = requests.get("https://serpapi.com/search", params=params, timeout=30)
        resp.raise_for_status()
        data = resp.json()
    except requests.exceptions.RequestException as e:
        log.error(f"Halaman {page}: Request SerpAPI gagal — {e}")
        return []

    if "error" in data:
        log.error(f"SerpAPI error: {data['error']}")
        return []

    return parse_serpapi_response(data)


def parse_serpapi_response(data: dict) -> list[dict]:
    """Ekstrak semua artikel dari respons SerpAPI."""
    articles = []
    raw = data.get("news_results", [])

    for item in raw:
        if "stories" in item:
            # Story cluster — ambil artikel di dalamnya
            cluster_title = item.get("title", "")
            for story in item["stories"]:
                articles.append(extract_serpapi_fields(story, cluster=cluster_title))
        else:
            articles.append(extract_serpapi_fields(item))

    return articles


def extract_serpapi_fields(item: dict, cluster: str = "") -> dict:
    """Ekstrak field dari satu item berita SerpAPI."""
    source = item.get("source", {})
    if isinstance(source, dict):
        source_name = source.get("name", "")
        source_icon = source.get("icon", "")
    else:
        source_name = str(source)
        source_icon = ""

    return {
        "judul":         item.get("title", ""),
        "link":          item.get("link", ""),
        "sumber":        source_name,
        "ikon_sumber":   source_icon,
        "tanggal":       item.get("date", ""),
        "snippet":       item.get("snippet", ""),
        "thumbnail":     item.get("thumbnail", ""),
        "cluster_topik": cluster,
        "deskripsi":     "",   # Diisi saat fetch konten
        "jumlah_kata":   0,
        "status_fetch":  "belum",
        "waktu_scrape":  datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
    }


def scrape_serpapi(total_pages: int) -> list[dict]:
    """Scraping semua halaman dari SerpAPI."""
    all_articles = []
    log.info(f"Mulai scraping SerpAPI: {total_pages} halaman | query: {QUERY}")

    for page in range(1, total_pages + 1):
        log.info(f"[SerpAPI] Halaman {page}/{total_pages}...")
        articles = fetch_serpapi_page(page)

        if not articles:
            log.warning(f"Halaman {page}: Tidak ada artikel. Berhenti.")
            break

        all_articles.extend(articles)
        log.info(f"  → {len(articles)} artikel | Total: {len(all_articles)}")

        if page < total_pages:
            time.sleep(SERPAPI_DELAY)

    # Hapus duplikat berdasarkan link
    seen = set()
    unique = []
    for art in all_articles:
        if art["link"] not in seen:
            seen.add(art["link"])
            unique.append(art)

    log.info(f"SerpAPI selesai. {len(unique)} artikel unik dari {len(all_articles)} total.")
    return unique


